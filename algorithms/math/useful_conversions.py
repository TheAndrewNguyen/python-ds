#algortihms for useful math and science conversions    

#inches to feet 
def inches_feet(i): 
  return i * 0.0833333    

def feet_inches(f): 
  return f * 12 


#Kelvins to celsuis 
def kelvin_celsuis(k):           
  return k - 273.15    

#celsuis to kelvins 
def celsuis_kelvin(c): 
  return c + 273.15    


